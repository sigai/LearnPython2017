'''位操作符'''

#   8   7   6   5   4   3   2   1   0#
#   256	128	64	32	16	8	4	2	1#2的n次幂
#   0   0   0   1   1   0   0   1   0    #50=32+16+2=2**5+2**4+2**1

#------------Binary AND------------
a = 50              #110010
b = 25              #011001
c = a & b           #010000 2**4=16
print(c)

#-----------Binary RIGHT SHIFT ------------
x = 240             #11110000
y = x >> 2          #00111100 2**5+2**4+2**3+2**2=32+16+8+4=60
print(y)            #相当于x除以2再除以2